      private void carregarComboCliente() {

        //Declarar uma varialvel
        //combodefaut para poder alterar dados do modelo

        DefaultComboBoxModel comboModel = (DefaultComboBoxModel) cbCliente.getModel();
        cbCliente.removeAllItems();

        //Buscar na base de dados os alunos cadastrados
        List<Cliente> clientes = new ArrayList<>();
        ClienteController cc = new ClienteController();
        clientes = cc.listAll();

        //Preencger o combo com os alunos que estao na lista

        for (int linha = 0; linha < clientes.size(); linha++) {
            Cliente c = clientes.get(linha);
            comboModel.setSelectedItem("  ----Selecione----  ");
            comboModel.addElement(c);
        }     
    }

    private void carregarComboCliente(Cliente cliente) {

        //Declarar uma varialvel
        //combodefaut para poder alterar dados do modelo
        DefaultComboBoxModel comboModel = (DefaultComboBoxModel) cbCliente.getModel();
        cbCliente.removeAllItems();
        //Buscar na base de dados os alunos cadastrados
        List<Cliente> clientes = new ArrayList<>();
        ClienteController cc = new ClienteController();
        clientes = cc.listAll();

        //Preencger o combo com os alunos que estao na lista

        for (int linha = 0; linha < clientes.size(); linha++) {
            Cliente c = clientes.get(linha);
            comboModel.addElement(c);
            if (cliente.getId() == c.getId()) {
                comboModel.setSelectedItem(c);
            }
        }
    }

