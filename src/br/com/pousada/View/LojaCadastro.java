    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // TODO add your handling code here:

        //Codigo para Adicionar ao banco os Campos Preenchidos Cadastro habitaçao
       Loja h = new Loja();

        if (!(txCodigo.getText().equals("") || (txCodigo.getText() == null))) {
            h.setId(Integer.parseInt(txCodigo.getText()));
        }

        h.setFormaPagamento(txPagamento.getText());
       h.setCliente((Cliente) cbCliente.getSelectedItem());
        h.setFuncionario((Funcionario) cbFuncionario.getSelectedItem());
        h.setVenda((Venda) cbVenda.getSelectedItem());

        //Editar funcionario e endereço************************/**************************************

       LojaController fc = new LojaController();
        if (h.getId() == 0) {

            int idVenda = fc.save(h);
            if (idVenda > 0) {

                JOptionPane.showMessageDialog(null, "Venda Efetuada com sucesso");
            }
        } else {
            int idVenda = fc.save(h);
            if (idVenda > 0) {

                JOptionPane.showMessageDialog(null, "Venda Efetuada com sucesso");
            }
        }

        //Construtor limpar campos
        limparDados();
    }//GEN-LAST:event_btAdicionarActionPerformed

